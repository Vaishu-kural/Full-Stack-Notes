#Bitwise operator
a = 2    #10
b = 3   #11

print("and",a & b)    #and    10 =>2
print("or",a | b)      #or       11=>3
print("xor",a ^ b)     #xor       
print("right shift",b>>1)     #right shift
print("left shit",a<<1)     #left shift

